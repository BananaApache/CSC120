package week13;

// Diamond class
public class Diamond {

    private int carats;
    private double dollarValue;


    public Diamond(int carats, double dollarValue) {
        this.carats = carats;
        this.dollarValue = dollarValue;
    } // end constructor


    public int getCarats() {
        return carats;
    } // end method getCarats


    public double getDollarValue() {
        return dollarValue;
    } // end method getDollarValue


} // end class Diamond
